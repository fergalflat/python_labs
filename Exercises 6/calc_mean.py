def calc_mean(list_numbers):
    '''
    Calcualte the mean of list of numbers

    Parameters
    ----------
    list_numbers : list
        list of numbers.

    Returns
    -------
    float
        mean of list of numbers.

    '''
   
    return sum(list_numbers)/len(list_numbers)

